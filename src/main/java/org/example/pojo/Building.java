package org.example.pojo;

public class Building {

    private int no;

    private int height;

    private int cengshu;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getCengshu() {
        return cengshu;
    }

    public void setCengshu(int cengshu) {
        this.cengshu = cengshu;
    }
}
